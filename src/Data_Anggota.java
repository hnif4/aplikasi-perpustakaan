    public void tampildata() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("NIS");
        tbl.addColumn("Nama");
        tbl.addColumn("jenis_kelamin");
        tbl.addColumn("telepon");
        tbl.addColumn("jurusan");
        tabelAnggota.setModel(tbl);
        try {
            Connection connection = Koneksi_db.getConnection();
            if (connection != null) {
                System.out.println("Koneksi berhasil!");
            } else {
                System.out.println("Koneksi gagal!");
            }
            Statement statement = connection.createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM data_anggota ORDER BY nama ASC");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("Nis"),
                    res.getString("Nama"),
                    res.getString("jenis_kelamin"),
                    res.getString("telepon"),
                    res.getString("jurusan"),});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Salah: " + e.getMessage());
            e.printStackTrace();
        }
    }

