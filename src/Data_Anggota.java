    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nis = eNis.getText();
        String nama = eNama.getText();
        String jenis_kelamin = rbLaki.isSelected() ? "Laki-Laki" : "Perempuan";
        String telepon = eTelepon.getText();
        String jurusan = (String) cbJurusan.getSelectedItem();

        // Validasi: Pastikan semua field diisi
        if (nis.isEmpty() || nama.isEmpty() || (!rbLaki.isSelected() && !rbPerempuan.isSelected()) || jurusan == null || telepon == null || telepon.isEmpty() || jurusan.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi lengkap datanya!");
        } else {
            try {
                Connection conn = Koneksi_db.getConnection();
                String sql = "INSERT INTO data_anggota (nis, nama, jenis_kelamin, telepon,jurusan) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, nis);
                pstmt.setString(2, nama);
                pstmt.setString(3, jenis_kelamin);
                pstmt.setString(4, telepon);
                pstmt.setString(5, jurusan);
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                tampildata(); // Refresh tabel
                bersih(); // Bersihkan form
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Gagal menyimpan data: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        int row = tabelAnggota.getSelectedRow();
        if (row >= 0) { // Memeriksa jika ada baris yang dipilih
            String nim = (String) tabelAnggota.getValueAt(row, 0);
            String nama = eNama.getText();
            String telepon = eTelepon.getText();
            String jurusan = (String) cbJurusan.getSelectedItem();

            // Pastikan salah satu radio button dipilih
            String jenis_kelamin;
            if (rbLaki.isSelected()) {
                jenis_kelamin = "Laki-Laki";
            } else if (rbPerempuan.isSelected()) {
                jenis_kelamin = "Perempuan";
            } else {
                JOptionPane.showMessageDialog(null, "Pilih jenis kelamin");
                return; // Hentikan proses jika jenis kelamin belum dipilih
            }

            try {
                Connection conn = Koneksi_db.getConnection();
                String sql = "UPDATE data_anggota SET nama = ?, jenis_kelamin = ?, telepon = ?, jurusan = ? WHERE nis = ?";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, nama);
                pstmt.setString(2, jenis_kelamin);
                pstmt.setString(3, telepon);
                pstmt.setString(4, jurusan);
                pstmt.setString(5, nim);   // Nim sebagai parameter terakhir

                pstmt.executeUpdate();

                // Menampilkan data terbaru di tabel
                tampildata();
                bersih();
                JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Gagal mengupdate data: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan diedit");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int row = tabelAnggota.getSelectedRow();
        if (row >= 0) { // Memastikan ada baris yang dipilih
            String nis = (String) tabelAnggota.getValueAt(row, 0);

            int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data dengan NIS: " + nis + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    Connection conn = Koneksi_db.getConnection();
                    String sql = "DELETE FROM data_anggota WHERE nis = ?";
                    PreparedStatement pstmt = conn.prepareStatement(sql);
                    pstmt.setString(1, nis);

                    pstmt.executeUpdate();

                    // Menampilkan data terbaru di tabel
                    tampildata();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Gagal menghapus data: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        // TODO add your handling code here:
        String cari = eCari.getText(); // Menggunakan nama variabel pencarian yang benar
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("nis");
        tbl.addColumn("nama");
        tbl.addColumn("jenis_kelamin");
        tbl.addColumn("telepon");
        tbl.addColumn("jurusan");
        
        tabelAnggota.setModel(tbl);

        try {
            Statement statement = (Statement) Koneksi_db.getConnection().createStatement();
            ResultSet res = statement.executeQuery(
                    "SELECT * FROM data_anggota WHERE nis LIKE '%" + cari + "%' "
                    + "OR nama LIKE '%" + cari + "%' "
                    + "OR jenis_kelamin LIKE '%" + cari + "%' "
                    + "OR telepon LIKE '%" + cari + "%' "
                    + "OR jurusan LIKE '%" + cari + "%' "
                    
            );

            // Bersihkan tabel sebelumnya
            tbl.setRowCount(0);

            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("nis"),
                    res.getString("nama"),
                    res.getString("jenis_kelamin"),
                    res.getString("telepon"),
                    res.getString("jurusan"),
                   
                });
            }

            if (tbl.getRowCount() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Data tidak ditemukan");
            }

            bersih();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal mencari data: " + e.getMessage());
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void tabelAnggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAnggotaMouseClicked
        // TODO add your handling code here:
        int row = tabelAnggota.getSelectedRow();
        if (row >= 0) { // Memastikan ada baris yang dipilih
            eNis.setText((String) tabelAnggota.getValueAt(row, 0));
            eNama.setText((String) tabelAnggota.getValueAt(row, 1));
            // Menetapkan status JRadioButton berdasarkan nilai dari tabel
            String jenisKelamin = (String) tabelAnggota.getValueAt(row, 2);
            if ("Laki-Laki".equals(jenisKelamin)) {
                rbLaki.setSelected(true);
                rbPerempuan.setSelected(false);
            } else if ("Perempuan".equals(jenisKelamin)) {
                rbLaki.setSelected(false);
                rbPerempuan.setSelected(true);
            } else {
                // Jika nilai tidak sesuai, pastikan tidak ada radio button yang dipilih
                rbLaki.setSelected(false);
                rbPerempuan.setSelected(false);
            }
            eTelepon.setText((String) tabelAnggota.getValueAt(row, 3));
            cbJurusan.setSelectedItem((String) tabelAnggota.getValueAt(row, 4));
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan diedit");
        }
    }//GEN-LAST:event_tabelAnggotaMouseClicked

