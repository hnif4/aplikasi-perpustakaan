    private void tampildata() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode Buku");
        tbl.addColumn("Judul Buku");
        tbl.addColumn("Pengarang");
        tbl.addColumn("Penerbit");
        tbl.addColumn("Tahun Terbit");
        tabelDataBuku.setModel(tbl);

        try {
            Connection connection = Koneksi_db.getConnection();
            Statement statement = connection.createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM data_buku ORDER BY judul_buku ASC");

            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("kd_buku"),
                    res.getString("judul_buku"),
                    res.getString("pengarang"),
                    res.getString("penerbit"),
                    res.getInt("tahun_terbit")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

