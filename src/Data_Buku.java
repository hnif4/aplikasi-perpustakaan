    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String cari = eCari.getText();
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Kode Buku");
    model.addColumn("Judul Buku");
    model.addColumn("Pengarang");
    model.addColumn("Penerbit");
    model.addColumn("Tahun Terbit");

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3310/java_perpustakaan", "root", "");
        Statement stmt = conn.createStatement();
        String sql = "SELECT * FROM data_buku WHERE judul_buku LIKE '%" + cari + "%' " +
                     "OR pengarang LIKE '%" + cari + "%' " + "OR tahun_terbit LIKE '%" + cari + "%' " +
                     "OR penerbit LIKE '%" + cari + "%'";
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("kd_buku"),       // Nama kolom di database
                rs.getString("judul_buku"),
                rs.getString("pengarang"),
                rs.getString("penerbit"),
                rs.getString("tahun_terbit")
            });
        }

        tabelDataBuku.setModel(model);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton4ActionPerformed

