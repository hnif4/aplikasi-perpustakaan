    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
    // Ambil nilai dari JTextField
String nis = eNis.getText();
String nama = eNama.getText();
String kode = eKd.getText();
String judul = eJudul.getText();

// Format tanggal yang benar
SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

// Ambil tanggal dari jDateChooser dan format sesuai dengan yang dibutuhkan
String tanggalPinjam = sdf.format(jDateChooser1.getDate());
String tanggalKembali = sdf.format(jDateChooser2.getDate());

if (nis.isEmpty() || nama.isEmpty() || kode.isEmpty() || judul.isEmpty() || tanggalPinjam.isEmpty() || tanggalKembali.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Isi semua data!");
    return;
}

try {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3310/java_perpustakaan", "root", "");
    String sql = "INSERT INTO data_peminjam (nis, nama, kd_buku, judul_buku, tanggal_pinjam, tanggal_kembali) VALUES (?, ?, ?, ?, ?, ?)";
    PreparedStatement pstmt = conn.prepareStatement(sql);
    pstmt.setString(1, nis);
    pstmt.setString(2, nama);
    pstmt.setString(3, kode);
    pstmt.setString(4, judul);
    pstmt.setString(5, tanggalPinjam);
    pstmt.setString(6, tanggalKembali);
    pstmt.executeUpdate();
    
    JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
    tampildata();
    bersih();
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
}
    }//GEN-LAST:event_simpanActionPerformed

