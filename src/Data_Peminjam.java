    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                                           
    String nis = eNis.getText();

    // Cek apakah NIS kosong
    if (nis.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Masukkan NIS yang ingin dihapus!");
        return;
    }

    // Tampilkan dialog konfirmasi sebelum menghapus
    int confirm = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data dengan NIS: " + nis + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    // Jika pengguna memilih 'Yes'
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3310/java_perpustakaan", "root", "");
            String sql = "DELETE FROM data_peminjam WHERE nis = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, nis);
            int deletedRows = pstmt.executeUpdate();

            if (deletedRows > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
            }

            // Refresh data di tabel dan bersihkan input
            tampildata();
            bersih();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        }
    } else {
        // Jika pengguna memilih 'No'
        JOptionPane.showMessageDialog(null, "Penghapusan dibatalkan.");
    }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String cari = eCari.getText();
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("NIS");
    model.addColumn("Nama");
    model.addColumn("Kode Buku");
    model.addColumn("Judul Buku");
    model.addColumn("Tanggal Pinjam");
    model.addColumn("Tanggal Kembali");

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3310/java_perpustakaan", "root", "");
        Statement stmt = conn.createStatement();
        String sql = "SELECT * FROM data_peminjam WHERE nama LIKE '%" + cari + "%' " +
                     "OR kd_buku LIKE '%" + cari + "%' " +
                     "OR judul_buku LIKE '%" + cari + "%' " +
                     "OR tanggal_pinjam LIKE '%" + cari + "%' " +
                     "OR tanggal_kembali LIKE '%" + cari + "%'";
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nis"),       // Nama kolom di database
                rs.getString("nama"),
                rs.getString("kd_buku"),
                rs.getString("judul_buku"),
                rs.getString("tanggal_pinjam"),
                rs.getString("tanggal_kembali")
            });
        }

        tabelPeminjam.setModel(model);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton4ActionPerformed

