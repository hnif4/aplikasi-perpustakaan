    private void tampildata() {
    DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("NIS");
    tbl.addColumn("Nama");
    tbl.addColumn("Kode Buku");
    tbl.addColumn("Judul Buku");
    tbl.addColumn("Tanggal Pinjam");
    tbl.addColumn("Tanggal Kembali");
    tabelPeminjam.setModel(tbl);

    try {
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3310/java_perpustakaan", "root", "");
        Statement statement = connection.createStatement();
        ResultSet res = statement.executeQuery("SELECT * FROM data_peminjam ORDER BY nama ASC");

        while (res.next()) {
            tbl.addRow(new Object[]{
                res.getString("nis"),
                res.getString("nama"),
                res.getString("kd_buku"),
                res.getString("judul_buku"),
                res.getDate("tanggal_pinjam"), // Tampilkan tanggal pinjam
                res.getDate("tanggal_kembali") // Tampilkan tanggal kembali
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        e.printStackTrace();
    }
}

    
